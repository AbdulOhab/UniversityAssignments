#include <iostream>

int main () {

   // iostream header.
   int n = 70;
   std::cout << std::dec << n << '\n';
   std::cout << std::hex << n << '\n';
   std::cout << std::oct << n << '\n';
   bool b = true;
   std::cout << std::boolalpha << b << '\n';
   std::cout << std::noboolalpha << b << '\n';
   return 0;
   return 0;
}
