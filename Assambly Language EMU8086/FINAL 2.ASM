.MODEL TINY

.DATA
X   DB '0'
STR DB 50 DUP('$')

.CODE

MAIN PROC
    MOV X   ,0
    MOV BL  ,0
	MOV SI  ,0    ; INPUT     INDEX
	MOV DI  ,0    ;OUTPUT  INDEX
	MOV CX  ,0    ;SIZE OF STRING
	MOV AH, 1

INP:
 	INT   21H
	MOV STR[SI],AL
	INC   CX        		;STRING SIZE
	INC    SI
	CMP AL,13
	JE     NL
	JMP  INP

NL:
	MOV AH,2
	MOV DL,10
INT    21H
MOV  DL,13
INT    21H



OP:
MOV DL,STR[DI]

CMP DL,32D
JE CT
BK:
INC   DI
;INT 21H
LOOP OP

JMP ED

CT:
INC BL
INC X
JMP BK

ED:


MOV AH,2H


    MOV DL,10
    INT 21H
    MOV DL,13
    INT 21H



MOV AL,BL
INC AL
MOV AH,00H
CALL OUTDEC




MAIN ENDP

INCLUDE OUTDEC.ASM
INCLUDE INDEC.ASM

END MAIN
