.MODEL TINY

.CODE

MAIN PROC   
    CALL INDEC
    PUSH AX
    CALL NEWLINE
    POP AX
    CALL OUTDEC
    
    
EXIT:
   MOV AH,4CH
   INT 21H
    
MAIN ENDP


INCLUDE INDEC.ASM
INCLUDE OUTDEC.ASM   

NEWLINE PROC
    MOV AH,2
    MOV DL,10
    INT 21H
    
    MOV DL,13
    INT 21H
    RET
NEWLINE ENDP

END MAIN