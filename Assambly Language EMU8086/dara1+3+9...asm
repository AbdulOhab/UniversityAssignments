.MODEL TINY

.DATA
X  DB '0' ;INPUT VALUE
Y  DB '0' ;INPUT VALUE
Z  DB '0' ;ROOT
S  DW '1' ;TEMP STORE FOR RESULT
T  DB '3' 
CT DB '1' ;COUNTER
ZP DW '0' ;STORE 2 FROM QUESTION 

.CODE

MAIN PROC

	CALL  INDEC
        CALL NEWLINE 
     
	
	MOV X,AL
	SUB X,48D
	MOV Y,AL
        MOV S,0001H  
	MOV Z,AL 
	MOV AH,2H 
	MOV ZP,0002H
	MOV AX,ZP    ;PREPARATION FROM MULTIPLICATION
	MUL X        ;(2*N)
	MOV ZP,AX    ;STORE THE VALUE
	
	CMP X,1      ;CHECK THE INPUT FOR 1
	JE FOR1:
	
	MOV CH, 00H  ;CLEAR CX FOR LOOP
	DEC X
	MOV CL,X     ;SET CX VALUE
;	MOV DX,ZP
        LP2:         
	MOV CT,CL    
	
	MOV AX, 0001H 
	MOV BX, 0003H 
	LP1:
	MUL BX        ;(AX*BX=AX)
	              
	DEC CT        ;DECREASE VALUE FOR NEXT MOVE 
	CMP CT,0      ;CHECK THE VALUE
	JNE LP1       ;IF MOT 0, GO BACK TO LP1
	
    ADD AX,S      ; ADD PREVIOUS VALUE 
    MOV S,AX      ; STORE THE VALUE TO S
    LOOP LP2      ; IF MOT 0, GO BACK TO LP2
    
    MOV BX,S      ;FINAL VALUE
    
    
   

	JMP ED:
	
	FOR1:
	MOV BX,0001H 
	
	ED:
	
	MOV AX,BX
	DIV ZP 
	
	MOV BX,AX

    CALL  OUTDEC

MAIN ENDP


INCLUDE INDEC.ASM
INCLUDE OUTDEC.ASM 

NEWLINE PROC
    
    MOV AH,2
    MOV DL,10
    INT 21H
    MOV DL,13
    INT 21H
    
    RET
NEWLINE ENDP

END 
